package com.Appzia.addpanda.Model;

public class trendingImageModel {

    int img1, img2, img3;

    public trendingImageModel(int img1, int img2, int img3) {
        this.img1 = img1;
        this.img2 = img2;
        this.img3 = img3;
    }

    public int getImg1() {
        return img1;
    }

    public void setImg1(int img1) {
        this.img1 = img1;
    }

    public int getImg2() {
        return img2;
    }

    public void setImg2(int img2) {
        this.img2 = img2;
    }

    public int getimg3() {
        return img3;
    }

    public void setimg3(int img3) {
        this.img3 = img3;
    }
}
